    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // Acción para editar un artículo seleccionado en la tabla
        if (jTableArticulos.getSelectedRow() != -1) {
            idArticuloActual = (int) jTableArticulos.getValueAt(jTableArticulos.getSelectedRow(), 0);
            accionAltaModificar = ACCIONMODIFICAR;

            ArticuloAccionesFrame.modificarBotonesAltaModificar(this);  // Cambia los botones de alta/modificar según la acción actual

            ArticuloAccionesFrame.rellenarFormulario(this);  // Rellena el formulario con los datos del artículo seleccionado
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar el artículo a editar");
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimpiarActionPerformed

         ArticuloAccionesFrame.borrarFormulario(this);
        if (accionAltaModificar == ACCIONMODIFICAR) {
             ArticuloAccionesFrame.reiniciarAccionAlta(this);
        }
    }//GEN-LAST:event_jButtonLimpiarActionPerformed

    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        if (jTableArticulos.getSelectedRow() != -1) {
            idArticuloActual = (int) jTableArticulos.getValueAt(jTableArticulos.getSelectedRow(), 0);
            int respuesta = JOptionPane.showConfirmDialog(this, 
                "¿Está seguro de que desea eliminar este artículo?", 
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION);
            
            if (respuesta == JOptionPane.YES_OPTION) {
                ArticuloAccesoApi.eliminarArticulo(this);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar el artículo a eliminar");
        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

