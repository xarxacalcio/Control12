    private void jButtonAnadirArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnadirArticuloActionPerformed
        ArticuloValidador articuloValidador = new ArticuloValidador(this);
        if (!articuloValidador.validarFormularioArticulo()) {
            JOptionPane.showMessageDialog(null, articuloValidador.getErrores());
        } else {
        if (accionAltaModificar == ACCIONALTA) {
            ArticuloAccesoApi.nuevoArticulo(this);  // Método para añadir un nuevo artículo
           ArticuloAccionesFrame.borrarFormulario(this);
        } else if (accionAltaModificar == ACCIONMODIFICAR) {
            ArticuloAccesoApi.modificarArticulo(this);  // Método para modificar un artículo existente
        }
        }


    }//GEN-LAST:event_jButtonAnadirArticuloActionPerformed

